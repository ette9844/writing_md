--WHERE
SELECT S.STU_NO, S.STU_NAME, T.SUB_NAME, E.ENR_GRADE
FROM STUDENT S, SUBJECT T, ENROL E
WHERE S.STU_NO = E.STU_NO
AND T.SUB_NO = E.SUB_NO;

--NATURAL JOIN
SELECT STU_NO, STU_NAME, SUB_NAME, ENR_GRADE
FROM ENROL
NATURAL JOIN STUDENT
NATURAL JOIN SUBJECT;

--JOIN ~ ON
SELECT S.STU_NO, S.STU_NAME, T.SUB_NAME, E.ENR_GRADE
FROM ENROL E 
JOIN STUDENT S ON S.STU_NO = E.STU_NO
JOIN SUBJECT T ON T.SUB_NO = E.SUB_NO;

--JOIN ~ USING
SELECT STU_NO, STU_NAME, SUB_NAME, ENR_GRADE
FROM ENROL E 
JOIN STUDENT USING(STU_NO)
JOIN SUBJECT USING(SUB_NO);


