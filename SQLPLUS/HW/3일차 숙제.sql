SELECT *
FROM STUDENT, ENROL
WHERE STUDENT.STU_NO = ENROL.STU_NO;

--NATURAL JOIN
SELECT *
FROM STUDENT NATURAL JOIN ENROL;

--JOIN ~ ON
SELECT *
FROM STUDENT JOIN ENROL ON STUDENT.STU_NO = ENROL.STU_NO;

--JOIN ~ USING
SELECT *
FROM STUDENT JOIN ENROL USING(STU_NO);


